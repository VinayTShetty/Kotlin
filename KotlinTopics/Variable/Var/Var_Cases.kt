Var (Mutable)

We can change the value of the variable declared using var keyword later in the programe.

fun main(args: Array<String>) {
    var name:String="Vinay T Shetty"
    /**
     * Changing the value of the variable
     */
    name="Lakshmi"
    println("Name=${name}")
}
