/**
  Only mutable properties can be applied for lateint.
 */

lateinit var name: String
fun main(args: Array<String>) {
    name="Vinay T Shetty"
    println(name)
}

*******************************************************************************************************************
/**
 * 'lateinit' modifier is allowed only on mutable properties
 */
lateinit val name: String
fun main(args: Array<String>) {
    name="Vinay T Shetty"
    println(name)
}
